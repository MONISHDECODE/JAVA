//WRITE A PROGRAM TO DEMO ALL TYPES OF VARIABLES

class VariableTypes
{
     int a=100; //INSTANCE VARIABLE
     static int b=200; //STATIC VARIABLE
     public int m1()
     {
          int c = 300; //LOCAL VARIABLE
          System.out.println("c = "+c);
          return 0;
     }
	 
     public static void main(String args[])
     {
          VariableTypes v = new VariableTypes ();
          System.out.println("a = "+v.a+"  b = "+b);
          v.m1(); //INVOKE THE m1 METHOD
     }
}

